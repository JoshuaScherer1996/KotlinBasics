package com.datphoenixstudios.kotlinbasicsvariables

fun main() {
    //creating the Variable with specific data types
    val title: String = "Android Masterclass"
    val myFloat: Float = 13.37F
    val myDouble: Double = 3.14159265358979
    val myAge: Int = 25
    val theYear: Int = 2022
    val myLong: Long = 18881234567
    val isHappy: Boolean = true
    val myCharacter: Char = 'a'

    //printing my variables
    println(title)
    println(myFloat)
    println(myDouble)
    println(myAge)
    println(theYear)
    println(myLong)
    println(isHappy)
    println(myCharacter)
}